package edu.kit.kastel;

/**
 * Represents the surface condition of a ski piste.
 *
 * @author uxuwg
 * @version 0.1
 */
public enum Surface {

    /**
     * A normal, well-groomed surface.
     */
    REGULAR,

    /**
     * An icy surface that offers less grip.
     */
    ICY,

    /**
     * A bumpy surface, such as a mogul piste.
     */
    BUMPY;
}