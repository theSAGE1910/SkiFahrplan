package edu.kit.kastel;

/**
 * Represents the difficulty level of a ski piste.
 *
 * @author uxuwg
 * @version 0.1
 */
public enum Difficulty {

    /**
     * An easy difficulty, generally suitable for beginners.
     */
    BLUE,

    /**
     * An intermediate difficulty for advanced skiers.
     */
    RED,

    /**
     * An expert-level difficulty that is very steep or demanding.
     */
    BLACK;
}